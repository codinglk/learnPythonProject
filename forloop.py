# for loop example

print("###for loop example1###")
for letter in "Lalit Kumar":
    print(letter)

print("###done###")

friends = ["Everett", "Aileen", "Mel"]

print("###for loop example2###")
for friend in friends:
    print(friend)

print("###done###")

print("###for loop example3###")
for index in range(10):
    print(index)

print("###done###")

print("###for loop example4###")
for index in range(3,10):
    print(index)

print("###done###")

print("###for loop example5###")
for index in range(len(friends)):
    print(friends[index])

print("###done###")

print("###for loop example6###")
for index in range(5):
    if index == 0:
        print("First Iteration")
    else:
        print("Not First")


print("###done###")


